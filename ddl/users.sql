-- DDL Table USERS untuk keperluan login (FirebirdSQL 5.0.4)
-- Nama tabel dan kolom disesuaikan dengan User.Contract di GEMINI.md

CREATE TABLE USERS (
    USER_ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USERNAME VARCHAR(50) NOT NULL UNIQUE,
    PASSWORD VARCHAR(255) NOT NULL, -- Menyimpan hash password (misalnya bcrypt / argon2)
    ROLE VARCHAR(30) NOT NULL,       -- Role pengguna (contoh: 'ADMIN', 'CASHIER')
    IS_ACTIVE BOOLEAN DEFAULT TRUE NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    MODIFIED_AT TIMESTAMP,
    DATATIMESTAMP TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL
);

