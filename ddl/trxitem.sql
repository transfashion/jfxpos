-- DDL Table TRX_ITEM (FirebirdSQL 5.0.4)
CREATE TABLE TRX_ITEM (
    TRX_ITEM_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TRX_ID BIGINT NOT NULL,
    ITEM_ID BIGINT NOT NULL,

 	ITEM_ART VARCHAR(100) NOT NULL,
    ITEM_COL VARCHAR(50),
	ITEM_SIZE VARCHAR(10),
    ITEM_DESCR VARCHAR(255),
	ITEM_PRICEGROSS DECIMAL(15, 2) DEFAULT 0.00 NOT NULL,
    ITEM_PRICE DECIMAL(15, 2) DEFAULT 0.00 NOT NULL,
    ITEM_DISC DECIMAL(15, 2) DEFAULT 0.00 NOT NULL,
	ITEM_IS_SPECIALPRICE BOOLEAN DEFAULT FALSE NOT NULL,

	PROMO_ID INTEGER,
	PROMO_DISC DECIMAL(15, 2) DEFAULT 0.00 NOT NULL,
	PROMO_VALUE DECIMAL(15, 2) DEFAULT 0.00 NOT NULL,

    QTY INTEGER DEFAULT 1 DEFAULT 0.00 NOT NULL,
    PRICE DECIMAL(15, 2) DEFAULT 0.00 NOT NULL,
    DISCOUNT DECIMAL(15, 2) DEFAULT 0.00 NOT NULL,
    SUBTOTAL DECIMAL(15, 2) DEFAULT 0.00 NOT NULL,
	
	PAYM_DISC DECIMAL(15, 2) DEFAULT 0.00 NOT NULL,
	



	
	
	CTG_ID INTEGER,
	UNIT_ID INTEGER,
	STRUCT_ID INTEGER,
	BRAND_ID INTEGER,
    IS_ACTIVE BOOLEAN DEFAULT TRUE NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    MODIFIED_AT TIMESTAMP,
    DATATIMESTAMP TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    
    CONSTRAINT FK_TRX_ITEM_TRX FOREIGN KEY (TRX_ID) REFERENCES TRX (TRX_ID) ON DELETE CASCADE,
    CONSTRAINT FK_TRX_ITEM_ITEM FOREIGN KEY (ITEM_ID) REFERENCES ITEM (ITEM_ID)
);

-- Indexing for performance
CREATE INDEX IDX_TRX_ITEM_TRX ON TRX_ITEM(TRX_ID);
CREATE INDEX IDX_TRX_ITEM_ITEM ON TRX_ITEM(ITEM_ID);
