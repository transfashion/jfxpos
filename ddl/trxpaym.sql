CREATE TABLE TRX_PAYM (
    TRX_PAYM_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TRX_ID BIGINT NOT NULL,
	PAYMDEV_ID INTEGER,
    PAYMETHOD_ID INTEGER NOT NULL,
    PAY_AMOUNT DECIMAL(15, 2) NOT NULL,
	PAY_VALUE DECIMAL(15, 2) NOT NULL,
	PAY_RETURN DECIMAL(15, 2) NOT NULL,
    CARD_NO VARCHAR(25),
	CARD_NAME VARCHAR(30),
    APPROVAL_CODE VARCHAR(20),
    IS_ACTIVE BOOLEAN DEFAULT TRUE NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    MODIFIED_AT TIMESTAMP,
    DATATIMESTAMP TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    
    CONSTRAINT FK_TRX_PAYM_TRX FOREIGN KEY (TRX_ID) REFERENCES TRX (TRX_ID) ON DELETE CASCADE,
    CONSTRAINT FK_TRX_PAYM_PAYMETHOD FOREIGN KEY (PAYMETHOD_ID) REFERENCES PAYMETHOD (PAYMETHOD_ID)
);

-- Indexing for performance
CREATE INDEX IDX_TRX_PAYM_TRX ON TRX_PAYM(TRX_ID);
CREATE INDEX IDX_TRX_PAYM_METHOD ON TRX_PAYM(PAYMETHOD_ID);
