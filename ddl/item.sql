-- DDL Table ITEM (FirebirdSQL 5.0.4)
CREATE TABLE ITEM (
    ITEM_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ITEM_ART VARCHAR(100) NOT NULL,
    ITEM_COL VARCHAR(50),
	ITEM_SIZE VARCHAR(10),
    ITEM_DESCR VARCHAR(255),
	ITEM_PRICEGROSS DECIMAL(15, 2) DEFAULT 0.00 NOT NULL,
    ITEM_PRICE DECIMAL(15, 2) DEFAULT 0.00 NOT NULL,
    ITEM_DISC DECIMAL(15, 2) DEFAULT 0.00 NOT NULL,
	ITEM_IS_SPECIALPRICE BOOLEAN DEFAULT FALSE NOT NULL,
	CTG_ID INTEGER,
	UNIT_ID INTEGER,
	STRUCT_ID INTEGER,
	BRAND_ID INTEGER,
    IS_ACTIVE BOOLEAN DEFAULT TRUE NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    MODIFIED_AT TIMESTAMP,
    DATATIMESTAMP TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL
);

-- Indexing for performance
CREATE INDEX IDX_ITEM_ART ON ITEM(ITEM_ART);
