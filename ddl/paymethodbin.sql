-- DDL Table PAYMETHODBIN (FirebirdSQL 5.0.4)
CREATE TABLE PAYMETHODBIN (
    PAYMETHODBIN_ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PAYMETHOD_ID INTEGER NOT NULL,
    BIN_PREFIX VARCHAR(10) NOT NULL,
    IS_ACTIVE BOOLEAN DEFAULT TRUE NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    MODIFIED_AT TIMESTAMP,
    DATATIMESTAMP TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    
    CONSTRAINT FK_PAYMETHODBIN_PAYMETHOD FOREIGN KEY (PAYMETHOD_ID) REFERENCES PAYMETHOD (PAYMETHOD_ID) ON DELETE CASCADE,
    CONSTRAINT UK_PAYMETHODBIN_PREFIX UNIQUE (BIN_PREFIX)
);

-- Indexing for performance
CREATE INDEX IDX_PAYMETHODBIN_PREFIX ON PAYMETHODBIN(BIN_PREFIX);

