-- DDL Table ITEM (FirebirdSQL 5.0.4)
CREATE TABLE ITEM (
    ITEM_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ITEM_ART VARCHAR(100) NOT NULL,
    ITEM_COL VARCHAR(50),
	ITEM_SIZE VARCHAR(10),
    ITEM_DESCR VARCHAR(255),

	ITEM_PRICEGROSS DECIMAL(15, 2) DEFAULT 0.00 NOT NULL,
    ITEM_PRICE DECIMAL(15, 2) DEFAULT 0.00 NOT NULL,
    ITEM_DISC DECIMAL(15, 2) DEFAULT 0.00 NOT NULL,
	ITEM_ISSPECIALPRICE BOOLEAN DEFAULT FALSE NOT NULL,

	ITEM_ISDISABLED BOOLEAN DEFAULT FALSE NOT NULL,

	CTG_ID INTEGER,
	CTG_NAME VARCHAR(255),
	UNIT_ID INTEGER,
	UNIT_NAME VARCHAR(255),
	STRUCT_ID INTEGER,
	STRUCT_NAME VARCHAR(255),
	BRAND_ID INTEGER,
	BRAND_NAME VARCHAR(255),
    
 	CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL, -- untuk keperluan apabila item dicreate langsung di module POS

    DATATIMESTAMP TIMESTAMP NOT NULL
);

-- Indexing for performance
CREATE INDEX IDX_ITEM_ART ON ITEM(ITEM_ART);
CREATE INDEX IDX_ITEM_DATATIMESTAMP ON ITEM(DATATIMESTAMP);

