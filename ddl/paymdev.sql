-- DDL Table PAYMDEV (FirebirdSQL 5.0.4)
CREATE TABLE PAYMDEV (
    PAYMDEV_ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PAYMDEV_NAME VARCHAR(100) NOT NULL,
    PAYMDEV_BANK VARCHAR(100),
    PAYMDEV_SERIALNO VARCHAR(100),
    PAYMDEV_PORT VARCHAR(50),
    PAYMDEVTYPE VARCHAR(15) NOT NULL CONSTRAINT CHK_PAYMDEVTYPE CHECK (PAYMDEVTYPE IN ('EDC', 'QR', 'VOU', 'ADV', 'CASH')),
    IS_ACTIVE BOOLEAN DEFAULT TRUE NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    MODIFIED_AT TIMESTAMP,
    DATATIMESTAMP TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL
);

-- Indexing for performance
CREATE INDEX IDX_PAYMDEV_TYPE ON PAYMDEV(PAYMDEVTYPE);

