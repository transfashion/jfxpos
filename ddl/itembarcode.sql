CREATE TABLE ITEM_BARCODE (
    ITEM_BARCODE_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ITEM_ID BIGINT NOT NULL,
    BARCODE VARCHAR(50) NOT NULL,
    BRAND_ID INTEGER,
    IS_ACTIVE BOOLEAN DEFAULT TRUE NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    MODIFIED_AT TIMESTAMP,
    DATATIMESTAMP TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT FK_ITEM_BARCODE_ITEM FOREIGN KEY (ITEM_ID) REFERENCES ITEM (ITEM_ID) ON DELETE CASCADE,
    CONSTRAINT UK_ITEM_BARCODE UNIQUE (BRAND_ID, BARCODE)
);

-- Indexing for performance
CREATE INDEX IDX_ITEM_BARCODE_VAL ON ITEM_BARCODE(BARCODE);
