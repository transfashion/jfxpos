-- Migration/Alter Table menggunakan EXECUTE BLOCK untuk FirebirdSQL (cek keberadaan field dengan IF EXISTS)
EXECUTE BLOCK AS
BEGIN
    -- Cek dan tambah MODIFIED_AT
    IF (NOT EXISTS (
        SELECT 1 FROM RDB$RELATION_FIELDS 
        WHERE TRIM(RDB$RELATION_NAME) = 'USERS' AND TRIM(RDB$FIELD_NAME) = 'MODIFIED_AT'
    )) THEN
    BEGIN
        EXECUTE STATEMENT 'ALTER TABLE USERS ADD MODIFIED_AT TIMESTAMP';
    END

    -- Cek dan tambah DATATIMESTAMP
    IF (NOT EXISTS (
        SELECT 1 FROM RDB$RELATION_FIELDS 
        WHERE TRIM(RDB$RELATION_NAME) = 'USERS' AND TRIM(RDB$FIELD_NAME) = 'DATATIMESTAMP'
    )) THEN
    BEGIN
        EXECUTE STATEMENT 'ALTER TABLE USERS ADD DATATIMESTAMP TIMESTAMP';
    END
END;
