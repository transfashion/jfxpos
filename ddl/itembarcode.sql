CREATE TABLE ITEMBARCODE (
    ITEMBARCODE_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ITEM_ID BIGINT NOT NULL,
    BARCODE VARCHAR(50) NOT NULL,
    BRAND_ID INTEGER,
    ITEMBARCODE_ISDISABLED BOOLEAN DEFAULT FALSE NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    MODIFIED_AT TIMESTAMP,
    DATATIMESTAMP TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT FK_ITEMBARCODE_ITEM_ID FOREIGN KEY (ITEM_ID) REFERENCES ITEM (ITEM_ID) ON DELETE CASCADE,
    CONSTRAINT UK_ITEMBARCODE UNIQUE (BRAND_ID, BARCODE)
);

-- Indexing for performance
CREATE INDEX IDX_ITEMBARCODE_BARCODE ON ITEMBARCODE(BARCODE);
CREATE INDEX IDX_ITEMBARCODE_DATATIMESTAMP ON ITEMBARCODE(DATATIMESTAMP);

