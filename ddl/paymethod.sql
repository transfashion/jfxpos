-- DDL Table PAYMETHOD (FirebirdSQL 5.0.4)
CREATE TABLE PAYMETHOD (
    PAYMETHOD_ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PAYMETHOD_NAME VARCHAR(100) NOT NULL,
    PAYMDEV_ID INTEGER,
    PAYMDEVTYPE VARCHAR(15) NOT NULL,
    IS_ACTIVE BOOLEAN DEFAULT TRUE NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    MODIFIED_AT TIMESTAMP,
    DATATIMESTAMP TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    
    CONSTRAINT FK_PAYMETHOD_PAYMDEV FOREIGN KEY (PAYMDEV_ID) REFERENCES PAYMDEV (PAYMDEV_ID),
    CONSTRAINT CHK_PAYMETHOD_DEVTYPE CHECK (PAYMDEVTYPE IN ('EDC', 'QR', 'VOU', 'ADV', 'CASH'))
);

-- Indexing for performance
CREATE INDEX IDX_PAYMETHOD_DEVTYPE ON PAYMETHOD(PAYMDEVTYPE);

